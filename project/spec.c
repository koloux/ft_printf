#include "libftprintf.h"

void	spec_get(char **fmt, char **opt)
{
	char	*cpy;

	if (*(*fmt) == 's' || *(*fmt) == 'c' || *(*fmt) == 'd' || *(*fmt) == 'i')
	{
		cpy = ft_strnew(1);
		cpy[0] = *(*fmt);
		*opt = cpy;
	}
}
